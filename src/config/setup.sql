CREATE TABLE IF NOT EXISTS [equipos] (
[id] INTEGER PRIMARY KEY AUTOINCREMENT,
[area_id] VARCHAR NULL,
[name] VARCHAR NULL,
[short_name] VARCHAR NULL,
[tla] VARCHAR NULL,
[crest_url] VARCHAR NULL,
[address] VARCHAR NULL,
[phone] VARCHAR NULL,
[website] VARCHAR NULL,
[email] VARCHAR NULL,
[founded] INT NULL,
[club_colors] VARCHAR NULL,
[venue] VARCHAR NULL,
[updated_at] VARCHAR NULL
);
